soma = 0
n = 1

while n <= 10:             # repete enquanto n for menor ou igual a 10
    soma = soma + n        # soma o valor de n à variável soma
    n = n + 1              # aumenta n em 1
    print(f"soma: {soma}") # mostra o valor atual de soma
    print(f"n : {n}")      # mostra o valor atual de n
